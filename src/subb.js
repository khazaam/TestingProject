import createMathOperation from './.internal/createMathOperation.js'

/**
 * subbstracts two numbers.
 *
 * @since 3.4.0
 * @category Math
 * @param {number} augend The first number in an addition.
 * @param {number} addend The second number in an addition.
 * @returns {number} Returns the total.
 * @example
 *
 * subb(6, 4)
 * // => 10
 */

/**
 * 
 * This code is our made by us, we wanted to have substraction operation
 * 
 */

const subb = createMathOperation((augend, addend) => augend - addend, 0)

export default subb
